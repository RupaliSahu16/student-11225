// 3.Program for sum of two numbers

#include <stdio.h>
int main(){
    int n1,n2,sum;
    printf("enter first number :");
    scanf("%d",&n1);
    printf("enter second number :");
    scanf("%d",&n2);
    printf("sum :\n");
    sum=n1+n2;
    printf("%d + %d = %d",n1,n2,sum);
    return 0;

}